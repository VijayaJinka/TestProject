package november27th2024;

public class Multiplication {
    public static void main(String[] args) {
        int a= 3;
        double b= 40.5;
        double c= a*b;
        System.out.println(c);
        //subtraction
        int d= 35;
        int e= 20;
        int f= d-e;
        System.out.println(f);
        //Division
        int dw= 100;
        float ef= 40;
        float z= dw%ef;
        System.out.println(z);
    }
}
